#Printing Pattern

print('G','F','G', sep='')

print('G','F','G', sep=' ')

print('G','F','G', sep='-')

#OR

print('G','F','G', sep=' ',end=' ')

print('G','F','G', sep='-')

#for formatting a date
print('09','12','2016', sep='-')

#another example
print('pratik','geeksforgeeks', sep='@')



print("1",sep=" ",end=" ")
print("2",sep=" ",end=" \n")

print( 3* ' ' + 5 * '*')



